    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(customer==null || job_name==null || type==null || size==null || Srate ==null || Sqty==null){
            JOptionPane.showMessageDialog(null, "Please fill the whole Form!", "INVOICE", JOptionPane.ERROR_MESSAGE);
        }
        else{
            String confirmationMessage = "Please confirm the details below:\n\n"
                                   + "Customer: " + customer + "\n"
                                   + "Job Name:" + job_name + "\n"
                                   + "Type: " + type + "\n"
                                   + "Size: " + size + "\n"
                                   + "Quantity: " + qty + "\n"
                                   + "Rate: " + rate + "\n\n"
                                   + "Do you want to proceed with transaction?";

            int option = JOptionPane.showConfirmDialog(null, confirmationMessage, "Confirmation", JOptionPane.YES_NO_OPTION);
            if(option == JOptionPane.YES_OPTION){ 
                Enterprise.Bill(customer, job_name, type, size, qty, rate);
                Enterprise.InvoiceItems(-1, customer, job_name, type, size, qty, rate);
                Enterprise.J.add(job_name);
                Enterprise.T.add(type);
                Enterprise.S.add(size);
                Enterprise.Q1.add(qty);
                Enterprise.R.add(rate);
                JOptionPane.showMessageDialog(null, "Transaction Successful.", "INVOICE", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                
                JOptionPane.showMessageDialog(null, "Cancelled by USER", "INVOICE", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed


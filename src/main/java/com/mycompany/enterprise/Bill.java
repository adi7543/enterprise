    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(!Enterprise.T.isEmpty()){
            try {
                int total = Enterprise.CalculateTotal();
                currentInvoiceID = Enterprise.Invoice(customer, total);
                Enterprise.UpdateInvoiceItems(currentInvoiceID);
                Enterprise.Generate(customer, total);
            } catch (Exception ex) {
                Logger.getLogger(Bill.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please save the transaction first!", "INVOICE", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    
